package intro;

public class Person {

    // Campos para almacenar el estado de un objeto Persona
    String name;
    int age;

    // Comportamiento
    // Método saludar, que responde al mensaje "saluda"
    public void greet() {
        System.out.println("Hola, me llamo " + name + " y tengo " + age + "años!");
    }

}
